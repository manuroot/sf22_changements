<?php

namespace Application\MyNotesBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * NotesColor
 *
 * @ORM\Table(name="notes_color")
 * @ORM\Entity(repositoryClass="Application\MyNotesBundle\Entity\NotesColorRepository")
 */

class NotesColor
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nom", type="string", length=20, nullable=false)
     */
    private $nom;


    public function __toString() {
        return $this->getNom();
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nom
     *
     * @param string $nom
     * @return NotesColor
     */
    public function setNom($nom)
    {
        $this->nom = $nom;
    
        return $this;
    }

    /**
     * Get nom
     *
     * @return string 
     */
    public function getNom()
    {
        return $this->nom;
    }
}